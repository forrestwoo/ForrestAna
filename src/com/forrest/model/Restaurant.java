package com.forrest.model;

public class Restaurant {
	private long id;
	private String name;
	private String statcont;
	private String commentNum;
	private String address;
	private String consumptionPerPerson;
	private String image;
	private String tele;
	private String businessHours;
	private String rid;
	private String city;
	private String source;
	private String cate;
	public String getRid() {
		return rid;
	}
	public void setRid(String rid) {
		this.rid = rid;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	public String getCate() {
		return cate;
	}
	public void setCate(String cate) {
		this.cate = cate;
	}
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getStatcont() {
		return statcont;
	}
	public void setStatcont(String statcont) {
		this.statcont = statcont;
	}
	public String getCommentNum() {
		return commentNum;
	}
	public void setCommentNum(String commentNum) {
		this.commentNum = commentNum;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getConsumptionPerPerson() {
		return consumptionPerPerson;
	}
	public void setConsumptionPerPerson(String consumptionPerPerson) {
		this.consumptionPerPerson = consumptionPerPerson;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getTele() {
		return tele;
	}
	public void setTele(String tele) {
		this.tele = tele;
	}
	public String getBusinessHours() {
		return businessHours;
	}
	public void setBusinessHours(String businessHours) {
		this.businessHours = businessHours;
	}
}
